# Arrays are used to store multiple values in one single variable:

cars = ["Ford", "Volvo", "BMW"]
print(cars)

# Access the Elements of an Array
x = cars[0]
print(x)

# The Length of an Array
y = len(cars)
print(y)

# Looping Array Elements
for z in cars:
    print(z)
    
# Adding Array Elements
cars.append("Honda")
print(cars)